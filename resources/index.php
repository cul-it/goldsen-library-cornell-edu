            	<h1>Resources</h1>
		      	<p>The Rose Goldsen Archive partners with many online institutions, exhibitions, and curators whose online resources provide invaluable reference materials on new media art.</p>
                
                <h2 id="asia"><a href="http://www.aaa.org.hk/" title="Asia Art Archive web site">Asia Art Archive</a></h2>
                <div class="floatright-image">
                	<a href="http://www.aaa.org.hk/" title="Asia Art Archive web site"><img src="/images/asia_logo.gif" alt="Asia Art Archive" border="0" /></a> 
                </div>
                <p>Initiated in 2000 as a direct response to the increasing number of Asian contemporary art exhibitions and events world-wide, the Asia Art Archive (AAA) is the first ever non-profit research centre in Hong Kong dedicated to documenting the recent history of visual art from the region within an international context.</p>
				<p>AAA maintains an online catalogue of material related to contemporary
				  art, including DVDs, CD-Roms, and Videos, especially from Mainland China,
				  Hong Kong, Taiwan, and Macau.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="citu"><a href="http://www.citu.info/" title="CiTu web site">CiTu, Federation of French University Labs in Art Creation and Emerging Media</a></h2>
                <div class="floatright-image">
                	<a href="http://www.citu.info/" title="CiTu web site"><img src="/images/citu_logo.gif" alt="CiTu" border="0" /></a> 
                </div>
                <p>CiTu is a collaboration between the three leading new media initiatives in French Universities: <a href="http://www.ciren.org/">cirEN, Interdisciplinary Research Center on the Aesthetics of Computing</a>, University of Paris 8;  <a href="http://creca.univ-paris1.fr/">LAM, Laboratory of Arts and Media</a>, University of Paris 1; <a href="http://paragraphe.univ-paris8.fr/fr/">Paragraph Laboratory</a>, University of Paris 8. These sites include extensive documentation of collaborative activities in art production and scholarly presentation since 1996.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="contactzones"><a href="http://contactzones.cit.cornell.edu/" title="Contact Zones web site">Contact Zones: The Art of CD-Rom</a></h2>
                <div class="floatright-image">
                	<a href="http://contactzones.cit.cornell.edu/" title="Contact Zones web site"><img src="/images/contactzones_logo.gif" alt="Contact Zones" border="0" /></a> 
                </div>
                <p>Contact Zones: The Art of CD-Rom was an extensive international exhibition, curated by Timothy Murray, that opened at Cornell University and toured internationally through 2004.  The bilingual online catalogue (English and Spanish) includes detailed descriptions and images of many of the CD-Roms held in the Goldsen Archive.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="ctheory"><a href="http://www.ctheory.net" title="CTHEORY web site">CTHEORY</a></h2>
                <div class="floatright-image">
                	<a href="http://www.ctheory.net" title="CTHEORY web site"><img src="/images/ctheory_logo2.gif" alt="ctheory.net" border="0" /></a> 
                </div>
                <p>Edited by Arthur and Marilouise Kroker, CTHEORY is an international peer-reviewed journal of theory, technology and culture, publishing articles, interviews, event-scenes and reviews of key books.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>

                <h2 id="virtual"><a href="http://www.virtualart.at/common/recentWork.do" title="Database of Virtual Art">Database of Virtual Art</a></h2>
                <div class="floatright-image">
                	<a href="http://www.virtualart.at/common/recentWork.do" title="Database of Virtual Art"><img src="/images/virtual_logo.gif" alt="Database of Virtual Art" border="0" /></a> 
                </div>
                <p>The Database of Virtual Art documents the rapidly evolving field  of digital installation art. This complex, research-oriented overview of immersive, interactive, telematic and genetic art has  been  conceived, organized, and maintained  by Dr. Oliver Grau, Humboldt University in Berlin.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="tv"><a href="http://www.experimentaltvcenter.org/history/index.html" title="Experimental Television Center web site">Experimental Television Center</a></h2>
                <div class="floatright-image">
                	<a href="http://www.experimentaltvcenter.org/history/index.html" title="Experimental Television Center web site"><img src="/images/tv_logo.gif" alt="Experimental Television Center" border="0" /></a> 
                </div>
                <p>The Experimental Television Center, located in Owego, New York, 20 miles from the Goldsen Archive, maintains the <span class="bold">Video History Project</span>, an extensive database that includes biographical and contextual information on many of the artists represented in the Goldsen Archive.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="fact"><a href="http://archive.fact.co.uk/" title="FACT Online">FACT Online</a></h2>
                <div class="floatright-image">
                	<a href="http://archive.fact.co.uk/" title="FACT Online"><img src="/images/fact_logo.gif" alt="FACT Online" border="0" /></a> 
                </div>
                <p>FACT, The Foundation for Art and Creative Technology in Liverpool,
                  England, is the UK's leading organization for the commissioning
                  and presentation of video and new media art forms. FACT
                  Online includes an archive of FACT's 6 Video Positive Festivals(1989-2000)
                  as well as other information on artists and events in video
                  and new media art.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="icc"><a href="http://www.ntticc.or.jp/Exhibition/2007/Openspace2007/archive/" title="ICC Online Archive Zone">ICC Online Archive Zone</a></h2>
                <div class="floatright-image">
                	<a href="http://www.ntticc.or.jp/Exhibition/2007/Openspace2007/archive/" title="ICC Online Archive Zone"><img src="/images/icc_logo.gif" alt="ICC Online Archive Zone" border="0" /></a> 
                </div>
                <p>NTT InterCommunication Center [ICC] is an innovative cultural facility in Tokyo Opera City Tower. ICC actively exhibits and promotes new forms of media art expression such as virtual reality and interactive technology. Archive Zone presents a multi-faceted history of ICC and its "HIVE" image recording archive of exhibitions and workshops.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="langlois"><a href="http://www.fondation-langlois.org/" title="Fondation Daniel Langlois web site">Langlois Foundation</a></h2>
                <div class="floatright-image">
                	<a href="http://www.fondation-langlois.org/" title="Fondation Daniel Langlois web site"><img src="/images/langlois_logo.gif" alt="Langlois Foundation" border="0" /></a> 
                </div>
                <p>The Fondation Daniel Langlois maintains an extensive Centre for Research and Documentation (CR+D) whose database search engine provides access to supplementary research materials on many artists and artworks represented in the Goldsen Archive.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="media"><a href="http://www.medienkunstnetz.de/mediaartnet/" title="Media Art Net web site">Media Art Net</a></h2>
                <div class="floatright-image">
                	<a href="http://www.medienkunstnetz.de/mediaartnet/" title="Media Art Net web site"><img src="/images/media_logo.gif" alt="Media Art Net" border="0" /></a> 
                </div>
                <p>Media Art provides an online survey of seminal interfaces between media and art. The Archive is directed by Dieter Daniels and Rudolf Frieling for the Goethe- Institut and ZKM.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="museum"><a href="http://www.arteonline.arq.br/" title="Museum of the Essential and Beyond That web site">Museum of the Essential and Beyond That</a></h2>
                <div class="floatright-image">
                	<a href="http://www.arteonline.arq.br/" title="Museum of the Essential and Beyond That web site"><img src="/images/museum_logo.gif" alt="Museum of the Essential and Beyond That" border="0" /></a> 
                </div>
                <p>Museum of the Essential and Beyond That is an inventive Brazilian archive of Latin American and International new media art and theory.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="newmedia"><a href="http://newmediafix.net/" title="Newmedia FIX web site">Newmedia FIX</a></h2>
                <div class="floatright-image">
                	<a href="http://newmediafix.net/" title="Newmedia FIX web site"><img src="/images/newmedia_logo.gif" alt="Newmedia FIX" border="0" /></a> 
                </div>
                <p>Newmedia FIX recommends online resources and projects, and offers news and occasional reviews, and periodically releseases in-depth texts on new media culture.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="noema"><a href="http://www.noemalab.org/index2.php" title="NOEMA web site">NOEMA, Tecnologie e Societ&#224;</a></h2>
                <div class="floatright-image">
                	<a href="http://www.noemalab.org/index2.php" title="NOEMA web site"><img src="/images/noema_logo.gif" alt="NOEMA" border="0" /></a>
                </div>
                <p>Edited by Pier Luigi Capucci, NOEMA is a website dedicated to providing resources and information about culture-new technology interrelations and influences.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="palazzo"><a href="http://www.palazzoartinapoli.net/" title="Palazzo delle Arti Napoli web site">Palazzo delle Arti Napoli</a></h2>
                <div class="floatright-image">
                	<a href="http://www.palazzoartinapoli.net/" title="Palazzo delle Arti Napoli web site"><img src="/images/palazzo_logo.jpg" alt="Palazzo delle Arti Napoli" border="0" /></a>
                </div>
                <p>The Palazzo sponsors contemporary exhibitions and houses a Documentation Center for the Contemporary Arts complete with archives, a library, a mediatheque, and cultural and experimental workshop activities.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="rhizome"><a href="http://rhizome.org/" title="Rhizome web site">Rhizome</a></h2>
                <div class="floatright-image">
                	<a href="http://rhizome.org/" title="Rhizome web site"><img src="/images/rhizome_logo.jpg" alt="Rhizome.org" border="0" /></a>
                </div>
                <p>Rhizome.org is an online platform for the global new media art community. Rhizome maintains extensive online databases of art and text that rely or reflect on innovative approaches to technology.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="v2"><a href="http://framework.v2.nl/archive/general/default.xslt" title="V2_Archive web site">V2_Archive</a></h2>
                <div class="floatright-image">
                	<a href="http://framework.v2.nl/archive/general/default.xslt" title="V2_Archive web site"><img src="/images/v2_logo.gif" alt="V2_Archive" border="0" /></a>
                </div>
                <p>The Archive portal at V2_, Institute for the Unstable Media provides documentation of activities since 1993 at the V2_ interdisciplinary center for art and media technology in Rotterdam. This includes a collection of some 15,000 photographs and a growing number of digitized video fragements, in addition to a large number of texts, images, and links.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="vectors"><a href="http://www.vectorsjournal.org/" title="Vectors web site">VECTORS: Journal of Culture and Technology in a Dynamic Vernacular</a></h2>
                <div class="floatright-image">
                	<a href="http://www.vectorsjournal.org/" title="Vectors web site"><img src="/images/vectors_logo.gif" alt="Vectors" border="0" /></a>
                </div>
                <p>Vectors maps the multiple contours of daily life in an unevenly digital era, crystallizing around themes that highlight the social, political, and cultural stakes of our increasingly technologically-mediated existence. The multimedia journal speaks to key debates across varied disciplines, including issues of globalization, mobility, power, and access. Operating at the intersection of culture, creativity, and technology, the journal focuses on the myriad ways technology shapes, transforms, reconfigures, and/or impedes social relations, both in the past and in the present.</p>
                <p class="small-gray right"><a href="#top" title="top">back to top</a></p>
                
                <h2 id="zkm"><a href="http://on1.zkm.de/zkm/e/" title="ZKM web site">ZKM</a></h2>
                <div class="floatright-image">
                	<a href="http://on1.zkm.de/zkm/e/" title="ZKM web site"><img src="/images/zkm_logo.gif" alt="ZKM" border="0" /></a>
                </div>
                <p>ZKM, The Center for Art and Media (Zentrum für Kunst und Medientechnologie) in Karlsruhe, Germany, responds to the rapid developments in information technology and today's changing social structures. Directed by Peter Weibel, the ZKM maintains an extensive website of its uniquue collection of new media art, much of which has been developed in the ZKM labs.</p>
