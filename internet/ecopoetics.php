            	<h1>Ecopoetics</h1>
                <h2>Ecopoetics Online Exhibition</h2>
                <p>Curated by Timothy Murray, Tom Shevory, and Patricia Zimmermann.</p>
                
                <a href="http://www.adhocsound.org/consumertopia.html" title="consumertopia web site"><img src="/images/ecopoetics.jpg" alt="image from consumertopia web site" border="0" /></a>
                
                <p>via <a href="http://www.ithaca.edu/fleff/" title="The Finger Lakes Environmental Film Festival web site: ">The Finger Lakes Environmental Film Festival (FLEFF)</a>:</p>
		      	<p> Selected artistic interventions from artists throughout the world explore the electronic interfaces between sustainability and environmental thought. Subsequently, they will be maintained in off-line form in the Rose Goldsen Archive of New Media Art, Cornell University Library.</p>
                <p>This international exhibition probes a series of questions about digitalities, visualities, and environments to create new landscapes for contemplation and action.</p>
				<p>How might new media environments and technological flows intervene in ecoculture and ecopolitics? What is the relationship between the techne of ecopoetics and the imperative of ecopolitics?</p>
                <p>How do Internet paradigms of speed, flow, and traffic impact notions of sustainability? Do mobile technologies and global positioning systems provide platforms for ecological activism? How can we decipher and comprehend the military’s utilization of ludic gaming systems for digital terror and ecological devastation?</p>
                <p>How might new media interventions offset media blackouts of the global ecology of war and public health degradation? How can the artistic mixing of ecological and poetic materials—organic, inorganic, technological, aural, and visual—create alternative and fertile environments in new media culture?</p>
                <p>The exhibition includes works by Judy Malloy, Diane Ludin, Ryan Griffis, Ian M. Clothier, Andrew Bucksbarg, Thorsten Knaub, Sam Smiley, Olga Kisselva, Ollivier Dyens, Joseph Rabie, Lillian Ball, Katerie Gladys, Annette Weintraub, Tiffany Holmes, Maria Damon and mIEKAL aND, Agricola Cologne, and Regina Célia Pinto.  We plan to archive the exhibit in The Rose Goldsen Archive of New Media Art, Cornell Library, following the Festival.</p>
                
