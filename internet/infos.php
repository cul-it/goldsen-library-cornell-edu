            	<h1>INFOS 2000</h1>
		      	<p>Murray, Timothy and Spiller, Teo, Off-line Net Art INFOS 2000, 2000 (CD-Rom PC).</p>
                <p>Timothy Murray and Teo Spiller launched this experimental venture of featuring international net.art at the INFOS2000 Festival, in Ljubljana, Slovenia, in an "off-line" 
format.</p>
				<p>The CD-Rom format of this net.art exhibition permitted its physical transfer among casual visitors to INFOS 2000 who might not otherwise have had the curatorial information or cultural interest to access these individual projects on the web.  The curators also offered it for use to individuals, public social centers, schools, and libraries in economically 
besieged zones that might not have had access to the high speed connections necessary for the ideal presentation of the majority of the net.art projects selected for exhibition.  The curators understood this gesture to address the digital divide while expanding the rhizomatic frontiers of digital art and its energetic flux.  The CD was also included in an edition of <span class="italic">M’ars: Magazine of the Museum of Modern Art, Ljubljana</span> (13, no 1-2, 2001).</p>
				<p>Curatorial essay by Timothy Murray, "Free Floating Rhizomes:  Off-Line Net Art"</p>
