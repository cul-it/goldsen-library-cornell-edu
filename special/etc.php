            	<h1>ETC: Experimental Television Center Archives</h1>                    
		      	<p>&quot;<a href="/etc">ETC: Experimental Television Center Archives</a>&quot; is a collection of  digitized tapes and ephemera of the electronic media work of over one  hundred artists who have worked in the Center's Residency Program in  Owego, New York, from 1971-2011. Directed by Ralph Hocking and Sherry  Miller Hocking, ETC is dedicated to the development of video and digital  instruments in the service of creative visual and sonic investigation  by artists from around the world. &nbsp;The collection offers a look at the  evolution of the unique artist-designed sound and image tools that are  the hallmark of the Center's studio and provides a view into the  constantly changing artistic processes and practices that have shaped  artistic experimentation in video over the years. This archive contains  works by the first generation of video and film artists - including  Barbara Hammer, Gary Hill, Jud Yalkut and Aldo Tambellini - as well as  contemporary works by artists such as Kristin Lucas, Lynne Sachs, and  Barbara Lattanzi. The works have been widely exhibited internationally  and received awards from festivals around the world. </p>
                	<img class="clear" src="/images/etv_center.jpg" alt="image from etc" />
				    <p>The digital Archive includes ETC: Experimental Television Center 1969-2009, an anthology of ten DVDs, drawn from the Archive's digitized tapes.  An essential component of the project, the digitizing of early video works recorded on obsolete formats, was performed through the Standby Program by Bill Seery. The project has received support from the Digitization Project Grants Program at the New York State Council on the Arts for which the Goldsen Archive is the official repository, mediaThe foundation, and the Daniel Langlois Foundation for Art, Science and Technology.</p>
