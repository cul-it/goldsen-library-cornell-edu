            	<h1>Elayne Zalis Video Studies Archive</h1>       
		          <img class="floatright" src="/images/zalis2.jpg" alt="image from Elayne Zalis" />
             
		      	<p>The "Elayne Zalis Video Studies Archive" is an extensive collection of study tapes, rare catalogues, and correspondence that documents the history of US video art. This expansive range of materials documents developments in independent American video throughout the 25-year period of its most significant production, from 1977-1992. These materials provided the background for Elayne Zalis's Ph.D. dissertation on humanities and the media arts at the University of Iowa.   In addition to 150 video tapes available for reference, the Zalis Archive includes a wide range of original exhibition catalogues and monographs on national and international video art, as well as research materials on independent distributors, festivals, and media centers in the 1980s. Reflecting the intellectual rigor of its organizer, Elayne Zalis, this curated documents the work of individual artists, group exhibitions, video festivals, distributors, and agencies and sources of commissioned video art, while providing a snapshot of the history of America video art: its production, exhibition, and critical evaluation. </p>
				<p>Elayne Zalis received her BA in 1975 from the University of Florida in Gainesville, and studied library science at Simmons College in Boston, Massachusetts, in 1976. After receiving her MA in journalism and scriptwriting at the University of Florida in 1983, Zalis enrolled at the University of Iowa, where she worked with the Special Collections Department of the University of Iowa Libraries on its Artists Television Project. In 1993, she completed her PhD at the University of Iowa in Interdisciplinary Studies, with a focus on humanities and the media arts.  The Elayne Zalis Archive includes the manuscripts and printed editions of Zalis's own multimedia quartet, Recycled Memories.</p>
				<p>NOTE: The Zalis Archive is being processed, and is not currently available.</p>
