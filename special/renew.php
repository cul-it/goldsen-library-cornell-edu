              	<p>This collection consists of artists' files submitted in application to the Renew Media Fellowships in New Media Art, supported by the <a href="http://www.rockfound.org/" title="Rockefeller Foundation web site">Rockefeller Foundation</a>. The fellowship was created in 2002 as a spin-off of the prestigious Rockefeller Fellowships in Film and Video. Nominated applicants are invited by <a href="http://renewmedia.org/" title="Renew Media web site">Renew Media</a> to submit proposals for work that centers on interactive, dynamic media, such as CD/DVD-Rom, web art, robotics, virtual reality, interactive installations, and software creativity. Six artists are chosen annually for fellowships from a pool of roughly 50 nominees.</p>
	            <p>Given the diversity of nominators and the prominence of applicants, this repository of the competition serves as a superb benchmark of American developments in the emergent field of new media art.</p>
	            <p>This collection is part of the Rare &amp; Manuscript Division's <a href="http://rmc.library.cornell.edu/collections/goldsen.html">Rose Goldsen Archive of New Media Art</a>, curated by Timothy Murray, Professor of Comparative Literature and English at Cornell University.</p>
	            <p>Cornell University Library has joined with Renew Media in an innovative digitization project of this collection. The project mounts online the dossiers of participating artists for consultation by users of the Goldsen website. Currently available are digitized copies of the analogue materials, from proposals to slides and supporting articles, of the 2003-2008 competitions.</p>
