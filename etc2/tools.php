      <h2>Tools and Technology</h2>
      <p>Many of ETC’s activities, including artist residencies, public outreach, and education programs, focused on the unique image processing tools of the Center’s media studio.  Many of these were developed and created at ETC.</p>
      <p>Beginning with modifications to existing equipment, the Center progressed to the design and construction of image processing equipment. One of the early projects involved the construction of the Paik/Abe Video Synthesizer, under the direction of Nam June Paik and Shuya Abe, for the TV Lab at WNET-TV. <em>(<a href="http://(http://www.experimentaltvcenter.org/etc-history)">ETC History</a>)</em></p>
      <p>Other tools available to artists in the ETC studio included:</p>
      <ul>
        <li>Jones Colorizer</li>
        <li>Jones Frame Buffer</li>
        <li>Jones Keyer</li>
        <li>Jones Sequencer</li>
        <li>Raster Manipulation Unit - Wobbulator</li>
        <li>Rutt / Etra Model RE-4 Video Synthesizer</li>
        <li>Sandin Image Processor</li>
      </ul>
      <p>ETC has created an <a href="http://www.experimentaltvcenter.org/early-media-instruments-set-8-dvds">8-DVD set</a> of demonstrations, documentation, and discussion of these tools, including demonstrations of their use and their effect on video images.</p>
