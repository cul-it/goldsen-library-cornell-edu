            	<h1>Virtual Seminars</h1>
                <p>In collaboration with projects sponsored by the Rose Goldsen Lecture Series of the <a href="http://www.arts.cornell.edu/" title="College of Arts and Sciences web site">College of Arts and Sciences</a>, the Goldsen Archive has initiated a series of Virtual Seminars. These seminars provide the opportunity for participants of Goldsen Lecture workshops and conferences to join in online conversation, via videostreaming, with participants gathered at alternating international venues. DVD recordings the Goldsen Virtual Seminars are available for consultation in the Archive.</p>
                <h2>Sound Cultures</h2>
                <p class="small-gray">Cornell University, September 11-13, 2003</p>
                <p><span class="bold">Sound Cultures: An International Workshop of Art and Theory</span> was a joint inaugural project of the Comparative Literature Theory Project and the Rose Goldsen Archive of New Media Art. The workshop introduced its Cornell audience to influential international artists and theorists who dwell on the cultural impact of sound in an electronic and digital age. Participants considered sound's importance in the era of visual studies, the cultural and ethnic specificity of sound fields and rhythms, the gender import of voice and spoken narrative, and the history and politics of electronic experimentations in sound.</p>
                <p>This workshop opened with an online seminar between speakers at the Cornell Workshop and sound artists in Sydney Australia brought together by Norie Neumark, who serves on our <a href="/about/advisory.php">International Advisory Board</a> and is Associate Professor of New Media Arts and Production at the University of Technology, Sydney (UTS).</p>
                <p>Please click here for a <a href="http://rmc.library.cornell.edu/events/soundcultures/" title="Sound Cultures web site">description and list of participants</a>.</p>
