<p align="justify">Chen Lingyang was born in Zhejiang Province in 1975,
and she studied in the Affliated High School of China Academy of Art,
Hangzhou, and Central Academy of Fine Arts, Beijing. She now lives in
Beijing.</p>
<p align="justify">Chen Lingyang mainly works with installation and
photography, and her works usually explore the beauty of female bodies
and their intrinsic meanings. In one of her major photography works,
“Twelve Flower Months”, what audience see through the traditional
Chinese mirrors are no longer the "beautiful" faces and bodies we are
accustomed to, but the female sex organs in menstruation. The
traditional poetic Chinese concept of twelve flower months provides a
metaphor for the female physiological truth of menstruation through the
twelve months of the year. With the traditional Chinese forms of
ancient mirrors and garden windows, gentle and sober disposal of light
and color around the sexual organs, directness of menstrual blood and
stimulation, Chen Lingyang breaks the tradition in a seemingly
traditional way and destroys the "femininity" with a seemingly
"feminine" approach. The direct selection of the female topic, the
attention to both "truth" and "beauty" of female bodies, and the
utilization of Chinese traditions and feminine form are all skillfully
and consciously presented.</p>
