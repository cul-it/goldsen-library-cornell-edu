<p>XU Bing (??) (born 1955)</p>
<p>Xu Bing was born in Chongqing, China and grew up in Beijing. In 1975
he was relocated to the countryside for two years during the Cultural
Revolution. In 1977 he enrolled in the Central Academy of Fine Art in
Beijing where he studied printmaking. He received an MFA from the
Central Academy in 1987. In 1990 he moved to the United States and he
still lives there today.</p>
<p>Using a wide range of media, Xu’s works and installation pieces
deftly manipulate various symbols to explore the value of art and
culture. He is especially interested in the meaning or meaninglessness
of the language, and in his most famous installation, <i>Book from the Sky</i>,
he invented 4000 seemingly Chinese but actually unintelligible
characters, hand-carving them into movable wooden blocks which he used
to print into volumes and scrolls. In another installation in 1994, <i>A Case Study of Transference</i>,
Xu employed a male and a female pig covered with false English and
Chinese scripts, mating on a pile of books with the presence of a group
of embarrassed audience. His recent works, such as <i>New English Calligraphy</i> and <i>The Glassy Surface of a Lake</i>,
also heavily involve play of language, or, in his own words, “tease of
cultural concepts and the limitation of human knowledge”.</p>

