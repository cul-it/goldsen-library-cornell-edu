            	<h1>About the Project</h1>
		      	<p>Named after the pioneering critic of the commercialization of mass media, the late Professor Rose Goldsen of Cornell University, the Archive was founded in 2002 by Timothy Murray to house international art work produced on CD-Rom, DVD-Rom, video, digital interfaces, and the internet. Its collection of supporting materials includes unpublished manuscripts and designs, catalogues, monographs, and resource guides to new media art.</p>
		      	<p>Emphasizing multimedia artworks that reflect digital extensions of twentieth-century developments in cinema, video, installation, photography, and sound, holdings include extensive special collections in American and Chinese new media arts, significant online and offline holdings in internet art, and the majority of works in the international exhibition, Contact Zones: The Art of CD-Rom. A novel research archive of international significance, the collection complements the holdings in the <a href="http://rmc.library.cornell.edu/" title="Rare and Manuscript Collections web site">Division of Rare and Manuscript Collections</a> of illuminated manuscripts and the early modern printed book, and adds to the breadth of its important collections in human sexuality, Asian Studies, and Media, Film, and Music.</p>
		      	<p>Extensive resources are available on the Goldsen website for a number of special collections: <a href="/resources/index.php#contactzones" title="Contact Zones">Contact Zones: The Art of CD-Rom</a>, <a href="/special/wen.php" title="Wen Pulin Achive of Chinese Avant-Garde Art">Wen Pulin Archive of Chinese Avant-Garde Art</a>, <a href="/special/renew.php" title="Renew/Rockefeller fellowships">Renew Media/Rockefeller New Media Art Fellowships</a>, <a href="/special/lynn.php" title="Lynn Hershman Archive">Lynn Hershman Archive</a>.</p>
		      	<p>Among Archive-sponsored pedagogical initiatives are its sponsorship of <a href="
http://adwhiteprofessors.cornell.edu/description.html" title="A.D. White Professor-at-Large">A. D. White Professor-at-Large</a>, Lynne Hershman Leeson, and its hosting of the <a href="/seminars/index.php" title="Rose Goldsen Virtual Seminars">Rose Goldsen Virtual Seminars</a>, which permit Cornell seminars and workshops to conduct online webstreaming with international colleagues.</p>
		      	<p>The Archive has profited from the financial support of The Rose Goldsen Fund, <a href="http://www.arts.cornell.edu/" title="College of Arts and Sciences web site">College of Arts and Sciences</a>, and the <a href="http://facultygrants.library.cornell.edu/" title="Faculty Grants web site">Cornell University Library Faculty Grants for Digital Library Collections</a> program.</p>
		      	<p>The curator reviews new materials for inclusion in the archive, and recommends the addition of reference materials that would contribute to the intellectual context of the archive.</p>
