                <h2 id="2">Rose Goldsen Archive of New Media Art Launches New Website</h2>
                <p class="small-gray">September 18, 2007</p>
                
                <p>Timothy Murray, Curator of The Rose Goldsen Archive of New Media Art at the Cornell University Library, is pleased to announce the public launch of the Archive's new comprehensive website: <a href="http://goldsen.library.cornell.edu">http://goldsen.library.cornell.edu</a>.</p>

				<p>The Goldsen website includes detailed listings of the Archive's extensive holdings of art and reference sources in a variety of media: CD/DVD-Rom, net art, video, installation, and book art. While most materials are available only to onsite consultants at Cornell University's Kroch Library  in Ithaca, New York, the website provides links to extensive resources in net and new media art as well as online access to materials from two extensive special collections:</p>

				<ul>
                	<li><span class="bold">The Wen Pulin Archive of Chinese Avant-Garde Art</span> consists of 360 hours of digitized video documenting the history of contemporary Chinese art, from video installations to performance pieces, from artist interviews to rock n'roll.</li>

					<li>The Goldsen Archive is the repository of the annual <span class="bold">Renew Media/Rockefeller Fellowships in New Media Art</span>. The Goldsen website permits access to a full listing of nominated artists and winners since 2002, as well as downloads of proposals and images by artists who have chosen to participate in the Goldsen's online program.</li>
				</ul>

				<p>Founded in 2002, the Goldsen Archive includes over 1000 individual new media artworks and artist dossiers. The website also documents the Goldsen Archive's commitments to hosting virtual seminars and pedagogical outreach at Cornell University.</p>

				<p>For further information, please contact Timothy Murray, Curator, Rose Goldsen Archive of New Media Art, Cornell University, at <a href="&#109;&#97;&#105;&#108;&#116;&#111;&#58;&#116;&#99;&#109;&#49;&#64;&#99;&#111;&#114;&#110;&#101;&#108;&#108;&#46;&#101;&#100;&#117;">&#116;&#99;&#109;&#49;&#64;&#99;&#111;&#114;&#110;&#101;&#108;&#108;&#46;&#101;&#100;&#117;</a>.</p>
                  
                <h2 id="1">Computerfinearts. com Off-line Archive</h2>
                <p class="small-gray">September 17, 2007</p>
                <p>The Rose Goldsen Archive of New Media Art at Cornell University announces the completion of the first phase of its archival partnership with Computerfinearts.com.  Available for consultation in the Goldsen Archive, Carl A. Kroch Library, Ithaca, New York, are the individual artworks housed online by Compterfinearts.com.</p>

				<p>This important collection of individual works by 95 artists will be preserved for posterity in the Goldsen Archive and complements the Archive's extensive holdings in art created for the internet, from its institutional online sponsorship of CTHEORY MULTIMEDIA to the Slovenian off-line exhibition, INFOS 2000.</p>

				<p>This partnership is the result of extensive discussions between Comperfineart's Doran Golan and Timothy Murray, Curator of the Goldsen Archive, regarding the fragile state of internet art and the importance of novel forms of its archivization.</p>
                
