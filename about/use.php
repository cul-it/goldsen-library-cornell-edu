		      	<p>The aim of the Goldsen Archive is to provide researchers, artists, faculty, and students with a better understanding of the transformation wrought on the artistic process by digital multimedia experimentation and development.</p>
      	  	    <p>The Archive is available to the public for consultation and access in the <a href="http://rmc.library.cornell.edu/about/about.html" title="Carl A. Kroch Library">Carl A. Kroch Library</a>. Users are free to consult materials on computer workstations and study tables during reading room hours. Artist books and drawings, manuscripts, and monographs can be ordered for consultation in the Elizabeth Reed Rare Book and Manuscript Reading Room. Artworks on CD/DVD-Rom, VHS/digital video, and net.art can be consulted in the adjacent Rare Book and Manuscript Media Room.</p>
      	  	    <p>We ask that patrons be sensitive to the situation that many materials in the Goldsen Archive contain dated software and digital platforms that might not be compatible with available screening devices and computers. Reference room staff may not be familiar with the particulars of these materials.</p>
                <p>We recommend that patrons rely on the Goldsen website and the <a href="https://catalog.library.cornell.edu/cgi-bin/Pwebrecon.cgi?DB=local&PAGE=First" title="Cornell University Library Catalogue">Cornell University Library Catalogue</a> to prepare lists of preferred viewing materials prior to visiting the Archive. Because Archive materials are catalogued differently (depending on whether they are in the General Collection or Special Collections), not all materials have individual access numbers in the Cornell Library catalogue &mdash;  materials in Special Collections are grouped according to collection (such as Renew Media/Rockefeller). All materials can be accessed in the catalogue either via individual author and title searches or keyword searches using author names. Artists and titles of online Internet Art listed in Goldsen collections are not available in the Cornell University Library Catalogue at this time. </p>
      	  	    <p>Please consult the <a href="http://www.library.cornell.edu/about/libhours.html#asia" title="reading room hours">reading room hours</a> for Kroch-Rare &amp; Manuscript Collections.</p>
