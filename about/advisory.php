            	<h1>International Advisory Board</h1>
		      	<p>The Goldsen Archive profits from the occasional advice and direction of an International Advisory Board of distinguished artists and curators:</p>
                
                <ul>
                	<li><span class="bold">Annette Barbier</span>, new media artist; Chair of the <a href="http://iam.colum.edu/" title="Department of Interactive Arts and Media web site">Department of Interactive Arts and Media</a>, Columbia College, Chicago, Illinois.</li>
                    
                    <li><span class="bold">Philippe Codognet</span>, specialist in art and new technologies and artificial intelligence; Professor of Computer Science, University of Paris 6, France, and Guest Professor, <a href="http://www.dmc.keio.ac.jp/en/" title="Research Institute for Digital Media and Content web site">Research Institute for Digital Media and Content</a>, University of Keio, Tokyo, Japan; liaison with new media centers in Japan and Korea.</li>
                    
                    <li><span class="bold">Alain Depocas</span>, Director of the Center for Research and Documentation, <a href="http://www.fondation-langlois.org/" title="Langlois Foundation web site">The Daniel Langlois Foundation for Art, Science, and Technology</a>, Montreal, Quebec, Canada; Director, DOCAM.</li>
                    
                    <li><span class="bold">Anne-Marie Duguet</span>, Professor of Aesthetics, Art, and Digital Media and Director of the <a href="http://creca.univ-paris1.fr/?page_id=2" title="Laboratory of Arts and Medias web site">Laboratory of Arts and Medias (LAM)</a>, University of Paris 1 (Sorbonne), Paris, France, with expertise in CD-ROM archiving as Director of <a href="http://www.anarchive.net/" title="Anarchive web site">Anarchive: Digital Archives on Contemporary Art</a></li>
                    
                    <li><span class="bold">Luci Eyers</span>, new media artist and Initiator of <a href="http://www.low-fi.org.uk/" title="Low-fi.org">Low-fi.org</a> artist collective focusing on net art, online mediation and distribution, commissioning and curation of net art projects, London, England.</li>
                    
                    <li><span class="bold">Maria Fernandez</span>, historian and theorist of digital art, <a href="http://www.arts.cornell.edu/histart/" title="Department of History of Art web site">Department of History of Art</a>, Cornell University, with emphases on feminism, postcolonial, and Latin American networks.</li>
                    
                    <li><span class="bold">Renate Ferro</span>, new media artist and Visiting Assistant Professor of Art, Cornell University; liaison with New York region media centers. Web site: <a href="http://www.renateferro.net/" title="Renate Ferro web site">www.renateferro.net</a></li>
                    
                    <li><span class="bold">Norie Neumark</span>, sound and <a href="http://www.out-of-sync.com/ " title="Out-of-Sync web site">new media artist</a> and Associate Professor of New Media Arts and Production, <a href="http://www.uts.edu.au/" title="University of Technology web site">University of Technology</a>, Sydney, with expertise in sound, installation, and long distance pedagogy; liaison with new media centers and archives in Australia and New Zealand.</li>
                    
                    <li><span class="bold">Regina Celia Pinto</span>, web artist and new media researcher in Rio de Janeiro, Brazil, creator and publisher of the archival project, <a href="http://www.arteonline.arq.br/" title="The Museum of the Essential and Beyond That web site">The Museum of the Essential and Beyond That</a>; liaison with Latin American new media centers and initiatives.</li>
                    
                    <li><span class="bold">Helen Thorington</span>, Boston, Massachusetts, Originator and co-producer of <a href="http://www.turbulence.org" title="Turbulence.org">Turbulence.org</a>, commissioning and exhibiting net art; originator and co-producer of <a href="http://www.somewhere.org" title="Somewhere.org">Somewhere.org</a>, an archive including New American Radio, which she founded; originator and director of Online Arts Performance Project, a network of "broadcast-enabled" art websites.</li>
                    
                    <li><span class="bold">Peter Weibel</span>, video and new media artist, and Director of <a href="http://www.zkm.de/" title="ZKM web site">ZKM (Zentrum für Kunst und Medientechnologie)</a>, Karlsruhe, Germany.</li>
                    
                    <li><span class="bold">Shin-Yi Yang</span>, curator, art historian, and Foreign Specialist, <a href="http://www.cafa.com.cn/asp/index.asp" title="Central Academy of Fine Arts web site">Central Academy of Fine Arts</a>, Beijing, China; liaison with Chinese and Asian new media initiatives.</li>
                </ul>

