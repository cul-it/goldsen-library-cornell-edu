      <h2>About ETC</h2>
      <div class="pull-right"><a href="http://www.experimentaltvcenter.org/"><img alt="ETC logo" src="img/fusion_starter_logo.png" style="width: 450px"></a></div>
              
      <p>The Experimental Television Center was founded in 1971 in Binghamton, NY, as an extension of Student Experiments in Television, a program created in 1969 at Binghamton University by Ralph Hocking. The center was moved to Owego, NY, in 1980.</p>

      <p>The Center’s programs addressed potential uses of the new technology in three major communities: artists; social, cultural and educational organizations; and interested citizens. Access to equipment, instruction in its operation and viewing and editing facilities as well as a series of regularly scheduled workshops were offered at no charge to facilitate a wide-ranging exploration of video. <em>(<a href="http://www.experimentaltvcenter.org/etc-history">ETC History</a>)</em></p>

      <div class="pull-left img-left"><img alt="Ralph and Sherry Hocking" src="img/ralph-sherry-hocking.jpg" /><p class="caption">Ralph and Sherry Hocking</p>
      </div>

      <p>From 1971 until the closing of the Residency and Grants Program in 2011, the ETC was an energetic focal point for media arts exploration, education, and practice in New York State.  The Center was the site of development of many innovative image processing tools, among them the Paik Abe synthesizer.  For decades, ETC supported a residency program for artists and students to work in its experimental media studio, with training on unique tools and access to a vast library of media artworks.  The Center’s activities also included gallery exhibits and conferences and grant programs to support artists and non-profit media arts programs.</p>

      <p>The spirit and energy of ETC persist, even after the Center’s official closing, in the <a href="http://www.experimentaltvcenter.org/history-about">Video History Project</a>, a research and education initiative, and in its involvement in preservation programs. <a href="http://signalculture.org/">Signal Culture</a> is a new nonprofit media arts center which expands on and reinterprets the ETC programs and processes.</p>

      <p>For more information, please see the <a href="http://www.experimentaltvcenter.org/">ETC website</a>.</p>
