          <p class="lead">Welcome to the video library and archives of the Experimental Television Center.</p>

          <p>From its founding in 1969 to its closing in 2011, the Experimental Television Center was one of North America’s preeminent centers for experimental video art. The Center provided education and outreach, independent art advocacy, fellowships, and a groundbreaking artist residency program.  Over the center’s 40-year history, the residency program allowed over 1500 media artists to use the Center’s studio, unique image processing tools, and video library.</p>

          <p>In 2011, Cornell’s <a href="http://goldsen.library.cornell.edu/">Rose Goldsen Archive of New Media Art</a> became the repository for ETC’s collection of over 3000 media artworks on videotape and DVD.  Though the Residency and Grants Program were closed in 2011, many of these artworks have been digitized for preservation and access, and all are available to the research community and the public through Cornell University Library’s Division of <a href="http://rare.library.cornell.edu/">Rare and Manuscript Collections</a>. This website provides access information and some historical and technological context for understanding these collections.</p>

          <p>For more thorough information about the Experimental Television Center, its history, its mission, and its ongoing activities, please see the <a href="http://www.experimentaltvcenter.org/etc-home">ETC website</a>. Though officially closed since 2011, the ETC and its staff remain an active force in promoting, preserving, and advocating for media experimentation and media art history.</p>

